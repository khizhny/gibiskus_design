let currentLang = localStorage.getItem("siteLanguage") === "en" ? "en" : "uk";
let selectedCategory = "all";
let requestSort = "new";

const elements = {
  city: document.querySelector("#requestCityFilter"),
  specialty: document.querySelector("#requestSpecialtyFilter"),
  format: document.querySelector("#requestFormatFilter"),
  categoryList: document.querySelector("#requestCategoryList"),
  requestList: document.querySelector("#requestList"),
  requestCount: document.querySelector("#requestCount"),
  profileSpecialty: document.querySelector("#profileSpecialty"),
  responseDrawer: document.querySelector("#responseDrawer"),
  responseThread: document.querySelector("#responseThread")
};

const cityOptions = ["all", "kyiv", "lviv", "odesa", "dnipro", "online"];
const specialtyOptions = ["all", "speech", "psychology", "aba", "occupational", "physical", "tutor", "early"];
const formatOptions = ["all", "inPerson", "online", "homeVisit"];

function t(key) {
  return translations[currentLang][key] || key;
}

function setStaticText() {
  document.documentElement.lang = currentLang;
  document.title = t("title");
  document.querySelector('meta[name="description"]')?.setAttribute("content", t("description"));
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    node.textContent = t(node.dataset.i18n);
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach((node) => {
    node.setAttribute("placeholder", t(node.dataset.i18nPlaceholder));
  });
  document.querySelectorAll("[data-i18n-aria-label]").forEach((node) => {
    node.setAttribute("aria-label", t(node.dataset.i18nAriaLabel));
  });
  document.querySelectorAll("[data-i18n-alt]").forEach((node) => {
    node.setAttribute("alt", t(node.dataset.i18nAlt));
  });
  document.querySelectorAll("[data-status-key]").forEach((node) => {
    node.textContent = t(node.dataset.statusKey);
  });
  document.querySelectorAll("[data-lang-switch]").forEach((button) => {
    const active = button.dataset.langSwitch === currentLang;
    button.classList.toggle("is-active", active);
    button.setAttribute("aria-pressed", String(active));
  });
}

function renderSelect(select, options, anyKey = "any") {
  if (!select) return;
  select.innerHTML = options
    .map((option) => `<option value="${option}">${option === "all" ? t(anyKey) : t(option)}</option>`)
    .join("");
}

function fillControls() {
  const previous = {
    city: elements.city?.value || "all",
    specialty: elements.specialty?.value || "all",
    format: elements.format?.value || "all",
    profileSpecialty: elements.profileSpecialty?.value || "speech"
  };

  renderSelect(elements.city, cityOptions, "any");
  renderSelect(elements.specialty, specialtyOptions, "anyDirection");
  renderSelect(elements.format, formatOptions, "anyDirection");
  if (elements.profileSpecialty) {
    elements.profileSpecialty.innerHTML = specialtyOptions
      .filter((option) => option !== "all")
      .map((option) => `<option value="${option}">${t(option)}</option>`)
      .join("");
  }

  if (elements.city) elements.city.value = previous.city;
  if (elements.specialty) elements.specialty.value = previous.specialty;
  if (elements.format) elements.format.value = previous.format;
  if (elements.profileSpecialty && specialtyOptions.includes(previous.profileSpecialty)) {
    elements.profileSpecialty.value = previous.profileSpecialty;
  }
}

function daysLeft(request) {
  const created = new Date(`${request.createdAt}T12:00:00`);
  const diff = Math.floor((new Date("2026-06-13T12:00:00") - created) / 86400000);
  return 30 - diff;
}

function filteredRequests() {
  const city = elements.city?.value || "all";
  const specialty = elements.specialty?.value || "all";
  const format = elements.format?.value || "all";
  let result = requestData.filter((request) => {
    if (city !== "all" && request.city !== city) return false;
    if (specialty !== "all" && request.specialty !== specialty) return false;
    if (selectedCategory !== "all" && request.specialty !== selectedCategory) return false;
    if (format !== "all" && !request.formats.includes(format)) return false;
    return true;
  });

  if (requestSort === "budget") {
    result = result.sort((a, b) => b.budget - a.budget);
  } else if (requestSort === "urgent") {
    result = result.sort((a, b) => Number(b.urgent) - Number(a.urgent) || daysLeft(a) - daysLeft(b));
  } else {
    result = result.sort((a, b) => new Date(b.createdAt) - new Date(a.createdAt));
  }

  return result;
}

function renderCategories() {
  if (!elements.categoryList) return;
  const items = ["all", "speech", "psychology", "aba", "occupational", "tutor", "early"];
  elements.categoryList.innerHTML = items
    .map((item) => {
      const count = item === "all" ? requestData.length : requestData.filter((request) => request.specialty === item).length;
      return `
        <button class="category-item ${selectedCategory === item ? "is-active" : ""}" type="button" data-category="${item}">
          <span>${item === "all" ? t("allRequests") : t(item)}</span>
          <span>${count}</span>
        </button>
      `;
    })
    .join("");
}

function renderRequests() {
  if (!elements.requestList || !elements.requestCount) return;
  const data = filteredRequests();
  elements.requestCount.textContent = data.length;

  if (!data.length) {
    elements.requestList.innerHTML = `
      <div class="listing-card">
        <div class="avatar">?</div>
        <div class="listing-main">
          <h3>${t("noRequestsTitle")}</h3>
          <p>${t("noRequestsText")}</p>
        </div>
      </div>
    `;
    return;
  }

  elements.requestList.innerHTML = data
    .map((request) => {
      const remaining = daysLeft(request);
      return `
        <article class="listing-card ${request.urgent ? "is-expiring" : ""}">
          <div class="avatar" aria-hidden="true">${request.initials[currentLang]}</div>
          <div class="listing-main">
            <h3>${request.title[currentLang]}</h3>
            <p>${request.family[currentLang]} · ${t(request.city)} · ${t(request.specialty)} · ${request.formats.map((format) => t(format)).join(", ")}</p>
            <p>${request.text[currentLang]}</p>
            <div class="tag-row">
              ${request.tags[currentLang].map((tag) => `<span class="tag">${tag}</span>`).join("")}
              <span class="tag">${t("documents")}</span>
            </div>
          </div>
          <div class="listing-meta">
            <span class="status ${request.urgent ? "warning" : ""}">${request.urgent ? t("urgent") : t("newRequest")}</span>
            <span>${t("available")}: ${request.available[currentLang]}</span>
            <span class="price">${request.budget.toLocaleString(currentLang === "uk" ? "uk-UA" : "en-US")} ₴</span>
            <span class="status">${remaining > 0 ? `${remaining} ${t("daysLeft")}` : t("willArchive")}</span>
            <button class="primary-button" type="button" data-respond="${request.id}">${t("reply")}</button>
          </div>
        </article>
      `;
    })
    .join("");
}

function renderAll() {
  setStaticText();
  fillControls();
  renderCategories();
  renderRequests();
}

function setLanguage(lang) {
  currentLang = lang === "en" ? "en" : "uk";
  localStorage.setItem("siteLanguage", currentLang);
  renderAll();
}

function openResponse(id) {
  const request = requestData.find((item) => item.id === Number(id));
  const responseTitle = document.querySelector("#responseTitle");
  if (!request || !elements.responseDrawer || !elements.responseThread || !responseTitle) return;
  responseTitle.textContent = `${t("responseTitle")}: ${request.family[currentLang]}`;
  elements.responseThread.innerHTML = `
    <div class="chat-bubble">${t("firstMessage")}</div>
    <div class="chat-bubble reply">${t("familyReply")}</div>
  `;
  elements.responseDrawer.classList.add("is-open");
  elements.responseDrawer.setAttribute("aria-hidden", "false");
  document.body.classList.add("drawer-open");
}

function closeResponse() {
  if (!elements.responseDrawer) return;
  elements.responseDrawer.classList.remove("is-open");
  elements.responseDrawer.setAttribute("aria-hidden", "true");
  document.body.classList.remove("drawer-open");
}

function bindEvents() {
  document.querySelector("[data-menu-toggle]")?.addEventListener("click", () => {
    document.querySelector(".site-header")?.classList.toggle("is-open");
  });

  document.querySelectorAll("[data-lang-switch]").forEach((button) => {
    button.addEventListener("click", () => setLanguage(button.dataset.langSwitch));
  });

  document.querySelector("#requestSearchForm")?.addEventListener("submit", (event) => {
    event.preventDefault();
    selectedCategory = "all";
    renderCategories();
    renderRequests();
  });

  [elements.city, elements.specialty, elements.format].forEach((control) => {
    control?.addEventListener("change", () => {
      selectedCategory = "all";
      renderCategories();
      renderRequests();
    });
  });

  document.querySelector("[data-reset-filters]")?.addEventListener("click", () => {
    selectedCategory = "all";
    if (elements.city) elements.city.value = "all";
    if (elements.specialty) elements.specialty.value = "all";
    if (elements.format) elements.format.value = "all";
    renderCategories();
    renderRequests();
  });

  elements.categoryList?.addEventListener("click", (event) => {
    const button = event.target.closest("[data-category]");
    if (!button) return;
    selectedCategory = button.dataset.category;
    if (elements.specialty) elements.specialty.value = "all";
    renderCategories();
    renderRequests();
  });

  document.querySelectorAll("[data-request-sort]").forEach((button) => {
    button.addEventListener("click", () => {
      requestSort = button.dataset.requestSort;
      document.querySelectorAll("[data-request-sort]").forEach((item) => item.classList.remove("is-active"));
      button.classList.add("is-active");
      renderRequests();
    });
  });

  elements.requestList?.addEventListener("click", (event) => {
    const button = event.target.closest("[data-respond]");
    if (button) openResponse(button.dataset.respond);
  });

  document.querySelector("[data-close-response]")?.addEventListener("click", closeResponse);
  elements.responseDrawer?.addEventListener("click", (event) => {
    if (event.target === elements.responseDrawer) closeResponse();
  });

  document.querySelector("#responseForm")?.addEventListener("submit", (event) => {
    event.preventDefault();
    const textarea = event.currentTarget.querySelector("textarea");
    elements.responseThread?.insertAdjacentHTML("beforeend", `<div class="chat-bubble">${textarea.value}</div>`);
    textarea.value = "";
    const status = document.querySelector("#responseStatus");
    if (status) {
      status.dataset.statusKey = "responseSent";
      status.textContent = t("responseSent");
    }
  });

  document.querySelector("#specialistProfileForm")?.addEventListener("submit", (event) => {
    event.preventDefault();
    const status = document.querySelector("#profileStatus");
    if (status) {
      status.dataset.statusKey = "profileSent";
      status.textContent = t("profileSent");
    }
    event.currentTarget.reset();
  });
}

